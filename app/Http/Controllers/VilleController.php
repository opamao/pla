    public function listeVille($id)
    {
        try {

            $ville = Ville::where('pays_id', $id)->select('villeid', 'libelle_ville')->get();

            return response()->json([
                'status' => true,
                'ville' => $ville
            ], 200);

        } catch (\Throwable $th) {
            return response()->json([
                'status' => false,
                'message' => $th->getMessage()
            ], 500);
        }
    }
